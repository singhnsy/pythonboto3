#Float Variables
num1 = 5.0
num2 = 2.5
addtion = num1 + num2   # Addition
print("add of two numbers:", addtion)

sub = num1 - num2   # substraction
print("substraction of two numbers:", sub)

multi = num1 * num2   # multiplication
print("multiplication of two numbers:", multi)

div = num1 // num2      # Division
print("division of two numbers:", div)

rem = num1 % num2       # reminder
print("reminder of two numbers:", rem)



