#This is used for generating pseudo-random numbers.
import random

# Generate a random float between 0.0 and 1.0
print(f"Random float: {random.random()}")    ## Print Random float number

# Generate a random interger between a range (e.g: 1 to 100)
print(f"Random Integer: {random.randint(1, 100)}")  ## Print Random number between 1 to 100.